def main():
    parser = argparse.ArgumentParser(
        description="DIANA Project: Project samples onto reference PCA space",
        prog="diana-project",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Example:
  # Single sample
  diana-project --sample results/validation_predictions/ERR10114862/
  
  # Multiple samples
  diana-project --sample results/validation_predictions/ERR*/
  
  # With custom output directory
  diana-project --sample results/validation_predictions/ERR10114862/ \\
                --output results/pca_projections/

This command requires that samples have already been processed by diana-predict.
It uses the existing unitig fraction files to project samples onto PCA space.
        """
    )
    parser.add_argument("--version", action="version",
                        version=f"%(prog)s {importlib.metadata.version('diana')}")

    # Input
    parser.add_argument(
        '--sample', '-s',
        type=str,
        nargs='+',
        required=True,
        help='Path(s) to diana-predict output directory(ies) (supports wildcards)'
    )
    
    # PCA reference
    parser.add_argument(
        '--pca-reference',
        type=Path,
        default=Path("models/pca_reference.pkl"),
        help='Path to PCA reference file (default: models/pca_reference.pkl)'
    )
    
    # Metadata
    parser.add_argument(
        '--metadata',
        type=Path,
        default=Path("paper/metadata/train_metadata.tsv"),
        help='Path to training metadata (default: paper/metadata/train_metadata.tsv)'
    )
    
    # Output
    parser.add_argument(
        '--output', '-o',
        type=Path,
        default=Path("results/pca_projection"),
        help='Output directory (default: results/pca_projection)'
    )
    
    # Neighbors
    parser.add_argument(
        '--k-neighbors',
        type=int,
        default=5,
        help='Number of nearest neighbors to highlight (default: 5)'
    )
    
    # Logging
    parser.add_argument(
        '--verbose', '-v',
        action='store_true',
        help='Enable verbose logging'
    )
    
    args = parser.parse_args()
    setup_logging(args.verbose)
    
    logger.info("=" * 70)
    logger.info("DIANA PROJECT: Project samples onto reference PCA space")
    logger.info("=" * 70)
    
    # Expand wildcards and collect all sample directories
    sample_dirs = []
    for pattern in args.sample:
        matches = glob(pattern)
        if not matches:
            logger.warning(f"No directories match pattern: {pattern}")
            continue
        for match in matches:
            match_path = Path(match)
            if match_path.is_dir():
                sample_dirs.append(match_path)
            else:
                logger.warning(f"Not a directory: {match}")
    
    if not sample_dirs:
        logger.error("No sample directories to process")
        sys.exit(1)
    
    logger.info(f"Found {len(sample_dirs)} sample(s) to project")
    
    # Load PCA reference (shared for all samples)
    logger.info("\n" + "=" * 70)
    logger.info("Loading reference PCA space")
    logger.info("=" * 70)
    
    reference_data = load_pca_reference(args.pca_reference)
    reference_pca = reference_data['pca_coordinates']
    reference_sample_ids = reference_data['sample_ids']
    
    # Load BLAST annotations (shared for all samples)
    blast_file = Path("results/feature_analysis/all_features_blast/blast_results.txt")
    blast_df = load_blast_annotations(blast_file, reference_data['unitig_ids'])
    if blast_df is None:
        logger.warning("\nBLAST annotations not available - unitig PCA plots will be skipped")
        logger.warning("To enable unitig plots, ensure BLAST results exist at:")
        logger.warning(f"  {blast_file}")
    
    # Load reference metadata
    if not args.metadata.exists():
        logger.error(f"Metadata not found: {args.metadata}")
        sys.exit(1)
    
    try:
        import polars as pl
        metadata = pl.read_csv(str(args.metadata), separator='\t')
        # Ensure sample order matches reference_sample_ids
        metadata = metadata.filter(pl.col('Run_accession').is_in(reference_sample_ids))
        metadata = metadata.sort(by='Run_accession')
    except ImportError:
        import pandas as pd
        metadata = pd.read_csv(args.metadata, sep='\t')
        metadata = metadata[metadata['Run_accession'].isin(reference_sample_ids)]
        metadata = metadata.sort_values('Run_accession').reset_index(drop=True)
    
    # Process each sample
    for i, sample_dir in enumerate(sample_dirs, 1):
        logger.info("\n" + "=" * 70)
        logger.info(f"Processing sample {i}/{len(sample_dirs)}: {sample_dir.name}")
        logger.info("=" * 70)
        
        try:
            # Load sample unitig vector
            unitig_vector, sample_id = load_sample_from_prediction_dir(sample_dir)
            
            # Load predictions if available
            predictions = load_predictions(sample_dir)
            if predictions:
                logger.info(f"  ✓ Loaded predictions from diana-predict")
            
            # Project onto PCA space
            sample_pca = project_sample(unitig_vector, reference_data, sample_id)
            
            # Find nearest neighbors
            nearest_ids, nearest_distances = compute_neighbors(
                sample_pca, 
                reference_pca, 
                reference_sample_ids,
                k=args.k_neighbors
            )
            
            logger.info(f"\n  Top {args.k_neighbors} nearest neighbors:")
            for j, (neighbor_id, distance) in enumerate(zip(nearest_ids, nearest_distances), 1):
                logger.info(f"    {j}. {neighbor_id} (distance: {distance:.2f})")
            
            # Generate plots
            sample_output_dir = args.output / sample_id
            plot_pca_projection(
                sample_pca=sample_pca,
                sample_id=sample_id,
                reference_pca=reference_pca,
                reference_sample_ids=reference_sample_ids,
                reference_metadata=metadata,
                output_dir=sample_output_dir,
                nearest_ids=nearest_ids,
                predictions=predictions
            )
            
            # Generate unitig PCA projection colored by species
            if blast_df is not None:
                plot_unitig_pca(
                    sample_id=sample_id,
                    sample_unitig_fraction=unitig_vector,
                    reference_data=reference_data,
                    blast_df=blast_df,
                    output_dir=sample_output_dir,
                    top_n_species=20
                )
                
                # Generate species abundance bar plot
                plot_species_abundance_barplot(
                    sample_id=sample_id,
                    sample_unitig_fraction=unitig_vector,
                    reference_data=reference_data,
                    blast_df=blast_df,
                    output_dir=sample_output_dir,
                    top_n_species=20
                )
            
            logger.info(f"\n  ✓ Results saved to: {sample_output_dir}")
            
        except Exception as e:
            logger.error(f"  ✗ Failed to process {sample_dir.name}: {e}")
            if args.verbose:
                import traceback
                traceback.print_exc()
            continue
    
    # Summary
    logger.info("\n" + "=" * 70)
    logger.info("PROJECTION COMPLETE")
    logger.info("=" * 70)
    logger.info(f"Processed: {len(sample_dirs)} sample(s)")
    logger.info(f"Output directory: {args.output}")
    logger.info("=" * 70)


if __name__ == "__main__":
    main()
