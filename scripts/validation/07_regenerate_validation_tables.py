def generate_latex_tables():
    """Generate all LaTeX tables via format_all_tables.py."""
    print("Generating LaTeX tables...")
    
    script = PROJECT_ROOT / "scripts" / "paper" / "format_all_tables.py"
    result = subprocess.run(
        [sys.executable, str(script), "--table", "all"],
        cwd=PROJECT_ROOT,
        capture_output=True,
        text=True
    )
    
    if result.returncode != 0:
        print(f"ERROR: {result.stderr}")
        return False
    
    print("  ✓ All LaTeX tables generated")
    return True


