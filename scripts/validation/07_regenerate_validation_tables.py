    print("="*80)
    
    success = True
    success &= update_final_model_performance()
    success &= generate_class_distribution()
    success &= update_supplementary_table()
    success &= generate_latex_tables()
    
    print("="*80)
    if success:
        print("✅ All tables regenerated successfully!")
        print("\nOutputs:")
        print("  📊 paper/tables/final_model_performance.csv")
